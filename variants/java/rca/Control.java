package rca;

///////////////////////////////////////////////////////////////////////////////
// (1) States
///////////////////////////////////////////////////////////////////////////////

// Status: FREEZE
enum State {
    INIT, IDLE, RUNNING, HALT, FAILURE, SHUTDOWN;

    String displayName() {
        return switch (this) {
            case INIT     -> "Init";
            case IDLE     -> "Idle";
            case RUNNING  -> "Running";
            case HALT     -> "Halt";
            case FAILURE  -> "Failure";
            case SHUTDOWN -> "Shutdown";
        };
    }
}

///////////////////////////////////////////////////////////////////////////////
// (2) Modes
///////////////////////////////////////////////////////////////////////////////

// Status: MUTABLE
enum Mode {
    NONE, DEBUG;

    String displayName() {
        return switch (this) {
            case NONE  -> "None";
            case DEBUG -> "Debug";
        };
    }
}

///////////////////////////////////////////////////////////////////////////////
// (3) Events
///////////////////////////////////////////////////////////////////////////////

// Status: MUTABLE
enum Event { NONE }

///////////////////////////////////////////////////////////////////////////////
// Control Plane
///////////////////////////////////////////////////////////////////////////////

// Status: FREEZE
public class Control {
    State state;
    Mode mode;
    Event event;

    Control() {
        this.state = State.INIT;
        this.mode  = Mode.DEBUG;
        this.event = Event.NONE;
    }
}
