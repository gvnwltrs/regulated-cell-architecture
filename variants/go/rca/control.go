///////////////////////////////////////////////////////////////////////////////
// Regulated Cell Architecture (RCA) - Go Variant
// Control Plane
///////////////////////////////////////////////////////////////////////////////

package rca

///////////////////////////////////////////////////////////////////////////////
// (1) States
///////////////////////////////////////////////////////////////////////////////

// Status: FREEZE
type State int

const (
	StateInit State = iota
	StateIdle
	StateRunning
	StateHalt
	StateFailure
	StateShutdown
)

func (s State) String() string {
	switch s {
	case StateInit:
		return "Init"
	case StateIdle:
		return "Idle"
	case StateRunning:
		return "Running"
	case StateHalt:
		return "Halt"
	case StateFailure:
		return "Failure"
	case StateShutdown:
		return "Shutdown"
	}
	return "Unknown"
}

///////////////////////////////////////////////////////////////////////////////
// (2) Modes
///////////////////////////////////////////////////////////////////////////////

// Status: MUTABLE
type Mode int

const (
	ModeNone Mode = iota
	ModeDebug
)

func (m Mode) String() string {
	switch m {
	case ModeNone:
		return "None"
	case ModeDebug:
		return "Debug"
	}
	return "Unknown"
}

///////////////////////////////////////////////////////////////////////////////
// (3) Events
///////////////////////////////////////////////////////////////////////////////

// Status: MUTABLE
type Event int

const EventNone Event = iota

///////////////////////////////////////////////////////////////////////////////
// Control Plane
///////////////////////////////////////////////////////////////////////////////

// Status: FREEZE
type ControlPlane struct {
	State State
	Mode  Mode
	Event Event
}

func DefaultControlPlane() ControlPlane {
	return ControlPlane{
		State: StateInit,
		Mode:  ModeDebug,
		Event: EventNone,
	}
}
