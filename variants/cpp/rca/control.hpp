/*******************************************************************************
 * Regulated Cell Architecture (RCA) - C++ Variant
 * Control Plane
 ******************************************************************************/

#ifndef RCA_CONTROL_HPP
#define RCA_CONTROL_HPP

namespace rca {

/*******************************************************************************
 * (1) States
 ******************************************************************************/

/* Status: FREEZE */
enum class State {
    Init,
    Idle,
    Running,
    Halt,
    Failure,
    Shutdown,
};

/*******************************************************************************
 * (2) Modes
 ******************************************************************************/

/* Status: MUTABLE */
enum class Mode {
    None,
    Debug,
};

/*******************************************************************************
 * (3) Events
 ******************************************************************************/

/* Status: MUTABLE */
enum class Event {
    None,
};

/*******************************************************************************
 * Control Plane
 ******************************************************************************/

/* Status: FREEZE */
struct ControlPlane {
    State state{State::Init};
    Mode  mode{Mode::Debug};
    Event event{Event::None};
};

} // namespace rca

#endif /* RCA_CONTROL_HPP */
